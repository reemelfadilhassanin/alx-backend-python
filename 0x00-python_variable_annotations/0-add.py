#!/usr/bin/env python3
"""
This module provides a function to add two floating point numbers.
"""

def add(a: float, b: float) -> float:
    """
    Add two floating point numbers.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
